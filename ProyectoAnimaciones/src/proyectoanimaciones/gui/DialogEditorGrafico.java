    private void menuGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_menuGuardarActionPerformed
        // TODO add your handling code here:
        if (pathColores != null && pathLienzo != null && pathPintar != null && pathTiempo != null) {
            System.out.println("Path: " + pathColores);
            System.out.println("Path: " + pathLienzo);
            System.out.println("Path: " + pathPintar);
            System.out.println("Path: " + pathTiempo);
            try {
                manejadorEscritura.escribirArchivoSalida(pathLienzo, manejadorEscritura.archivoLienzo(lista));
                manejadorEscritura.escribirArchivoSalida(pathColores, manejadorEscritura.archivoColores(lista));
                manejadorEscritura.escribirArchivoSalida(pathTiempo, manejadorEscritura.archivoTiempo(lista));
                manejadorEscritura.escribirArchivoSalida(pathPintar, manejadorEscritura.archivoPintar(lista));
                JOptionPane.showMessageDialog(this, "Se han guardado los cambios.");
            } catch (IOException ex) {
                Logger.getLogger(DialogEditorGrafico.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        
    }//GEN-LAST:event_menuGuardarActionPerformed

